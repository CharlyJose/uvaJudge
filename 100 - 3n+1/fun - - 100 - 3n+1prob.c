#include <stdio.h>


void drawGraph(float length){
	int halfSpace=length;

	printf("\n|");
	while(length>0){
			printf("-");
			length--;
	}
	if(length>halfSpace)
		printf("-");

}

int calc(long low, long high){

	int countf=0, countp;
	long n,i;
	float iPrint;
	int iBaseLine;
	int k;

	n= high;
	printf("\n\n");
	for(k=1; k<61; k++){
		if(k<30)
			printf(">");
		if(k>30)
			printf("<");
		if(k==30)
			printf("GRAPH");
	}
	printf("\n\n");


	/* THE HEART OF THE PROGRAM */
	for(i= high; i>= low; i--){
		n=i;
		countp=1;
		while(n>1){
			if(n%2 == 0)		/*EVEN*/
				n= n/2;
			else			/*ODD*/
				n= 3*n+1;		
			countp++;
		}	
		if(countp> countf)
			countf=countp;	

		/* n-countp GRAPH CODE */	
		iPrint=countp;
		printf("\n");
		printf("+");
		drawGraph(iPrint*7/10);
		printf("%d\n", countp);	
	}
	/* n-countp GRAPH CODE */	
	iBaseLine= countf+20;
	drawGraph(iBaseLine*7/10);
	
	printf("\n\n");
	
return countf;
}


int main(void){
	long low,high;
	int i;
	for(i=1; i<5; i++){
		printf("\nENTER: ");
		scanf("%ld %ld", &low, &high);
		if(low>high){
			low= low+high;
			high= low-high;
			low= low-high;
		}
		printf("\nRESULT: %ld %ld %d\n\n",low, high, calc(low, high));
	}

	return 0;
}


